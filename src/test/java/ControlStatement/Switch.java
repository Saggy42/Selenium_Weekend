package ControlStatement;

public class Switch {
	
	public static void main(String[] args) {
		
		String name="Kajal";
		
		switch (name) {
		
		case "Sagar":
			System.out.println("Name is Sagar");
			break;
			
		case "Kajal":
			System.out.println("NAme is Kajal");
			break;
			
		case "Shweta":
			System.out.println("NAme is Sagar");
			break;
			
		default:
			System.out.println("You are not technogeek");
			
		
		
		}
		
		
	}

}
