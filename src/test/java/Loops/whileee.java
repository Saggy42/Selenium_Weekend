package Loops;

public class whileee {
	
	public static void main(String[] args) {
		
		int i=11;
		
		do {						//Do comes with while statement.
			System.out.println(i);
			i++;
			
		}while(i<=10);				//Even if while condition is false,
									//it will execute do statement
		
		
		
	}
		
		
		
	

}
