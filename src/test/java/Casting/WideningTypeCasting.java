package Casting;

public class WideningTypeCasting {
	
	public static void main(String[] args) {
		
		
		int x= 1000;
		
		long y=x;
		
		double z=y;
		
		System.out.println("The value before widening is "+x);
		
		System.out.println("The value after converting to long is "+y);
		
		System.out.println("The value after converting to double is "+z);
		
		
		
		
		
		
	}

}
